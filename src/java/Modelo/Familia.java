
package Modelo;

public class Familia {
    private int IdFam;
    private String NomFam;

    public Familia() {
    }
     
    public int getIdFam() {
        return IdFam;
    }

    public void setIdFam(int IdFam) {
        this.IdFam = IdFam;
    }

    public String getNomFam() {
        return NomFam;
    }

    public void setNomFam(String DesFam) {
        this.NomFam = DesFam;
    }
    
    
}
