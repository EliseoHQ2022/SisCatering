
package Modelo;
public class Resumen {
    private int CantidadC;
    private double Ventadia;
    private int CAtendidodia;
    private double ventames;

    public Resumen() {
    }

    public int getCantidadC() {
        return CantidadC;
    }

    public void setCantidadC(int CantidadC) {
        this.CantidadC = CantidadC;
    }

    public double getVentadia() {
        return Ventadia;
    }

    public void setVentadia(double Ventadia) {
        this.Ventadia = Ventadia;
    }

    public int getCAtendidodia() {
        return CAtendidodia;
    }

    public void setCAtendidodia(int CAtendidodia) {
        this.CAtendidodia = CAtendidodia;
    }

    public double getVentames() {
        return ventames;
    }

    public void setVentames(double ventames) {
        this.ventames = ventames;
    }
    
}
